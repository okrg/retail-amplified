<h1>:: Tips </h1>
<div class="databox">
<p>Because of the flexible nature of web browsers, and the complex inner workings of the Collaboration Network, the Back button does not operate as expected sometimes. You might get a message that says Warning: This page has expired. </p>
<p><img src="images/help_back.jpg" width="162" height="108"></p>
<p>&nbsp;</p>
<p>To avoid such errors, you should get into the habit of using the menu to go back to <strong>Home. </strong>This is the simplest way to navigate the file structure. </p>
<p><img src="images/help_menu.jpg" width="272" height="89"></p>
<p>&nbsp;</p>
<p>You might be tempted to copy and paste a URL to send a colleague of yours to a file or photo. This is not going to work as expected most of the time. Because of the unpredictable nature and flexibility of the web browser, sending a URL as you see it from the address bar will likely cause errors for the other person. Instead, you should instruct them to log in to the system with their own user ID and password and navigate to the files as you would normally. </p>
<p><img src="images/help_nocopy.jpg" width="500" height="277"></p>
